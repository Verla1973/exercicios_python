'''
Tipos de dados
str - string
int - inteiro
float - real/ponto flutuante
bool - booleano/lógico
'''
print('Verlã', type('Verlã'))
print(10, type(10))
print(25.23, type(25.23))
print('l' == 'L', type('l' == 'L'))

# String: nome
nome = 'Verlã'
print(nome, type(nome))

# Idade: int
idade = 47
print(idade, type(idade))

# Altura: float
altura = 1.87
print(altura, type(altura))

# É maior de idade
print(idade > 18, type(idade > 18))
